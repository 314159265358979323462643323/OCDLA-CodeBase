        </h2>

        <div class="editableSection mw-collapsible mw-made-collapsible" id="mw-customcollapsible-8">
            <p>If the need arises to reschedule the hearing date, please note that if the petitioner files the request, the client’s license will be suspended within the time period indicated on the implied consent form. If the officer is unavailable and the hearing is rescheduled, the client’s license suspension is withdrawn until the outcome of the hearing. Reasons for allowing the department to reschedule the hearing are covered in ORS 813.440 and includes the inability of the attorney to appear due to the attorney’s illness, vacation or scheduling conflict arising from other court or administrative hearing appearances.
            </p>
        </div>

        <h2 class="mw-customtoggle-9">
            <span class="mw-headline" id="E._Servicemembers"><b>E. Servicemembers</b></span> <span class="editsection editSectionInactive" style="float:none" id="editSection-9">[<a href="/index.php?title=DUII_Notebook:Chapter_3&amp;action=edit&amp;section=9" class="editSectionLinkInactive" id="editSectionAnchor-9" onclick="MediaWikiModule__EditSectionClearerLink_ExecuteEditLink(event,this);" title="Edit section: E. Servicemembers">edit</a>]<span class="editSectionChromeInactive" id="editSectionChrome-9">⇲</span></span>
        </h2>

        <div class="editableSection mw-collapsible mw-made-collapsible" id="mw-customcollapsible-9">
            <p>If the client is in the military, different rules will apply. Pursuant to ORS 183.413(a), a member of the Armed Forces (Army, Navy, Air Force, Marines, Coast Guard and National Guard) may be subject to the protection of the Servicemembers’ Civil Relief Act (SCRA). The SCRA provides certain protections to servicemembers and their dependents, if specific conditions are met. If your client qualifies, he or she may be entitled to have the license suspension matter stayed. You will need to get a copy of the client’s service orders as it pertains to the specific condition.
            </p>
